// 🐶 crée une fonction `calculAire` (aire = longeur x largeur) qui sera exportée par defaut
// 🤖 export default function
export default function calculAire(longueur, largeur) {
  return longueur * largeur;
}

// 🐶 crée une fonction `calculPerimetre` (perimètre = longeur x largeur x 2) qui sera exportée
// 🤖 export function
export function calculPerimetre(longueur, largeur) {
  return (longueur + largeur) * 2;
}   
// 🐶 crée une constante `TEMP_MAX` = 60 qui sera exportée
// 🤖 export const
